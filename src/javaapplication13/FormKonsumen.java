    private void tombolReloadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolReloadActionPerformed
        // TODO add your handling code here:
        Connection connection = null;
        try {
            // buat koneksi ke server mysql.
            Class.forName("com.mysql.cj.jdbc.Driver");
            connection = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/tokobuku", "root", "");

            // siapkan objek statement untuk query
            Statement statement;
            statement = connection.createStatement();
            ResultSet resultSet;

            // eksekusi query
            resultSet = statement.executeQuery("select * from konsumen");

            // baca dan tampilkan data dari query
            String id;
            String nama;
            String telepon;
            
            DefaultTableModel model = (DefaultTableModel)tabelKonsumen.getModel();
            while(model.getRowCount()>0) {model.removeRow(0);}
            
            while (resultSet.next()) { // baca data & geser kursor ke record selanjutnya
                id = resultSet.getString("id"); // baca kolom "kode"
                nama = resultSet.getString("nama"); // baca kolom "nama"
                telepon = resultSet.getString("telepon"); // baca kolom telepon di database
                
                String data[] = {id, nama, telepon};
                model.addRow(data);
            }

            // hapus objek dan koneksi
            resultSet.close();
            statement.close();
            connection.close();
        }
        catch (Exception exception) {
            // tampilkan pesan error (jika terjadi kesalahan)
            System.out.println(exception);
        }
    }//GEN-LAST:event_tombolReloadActionPerformed

