    public void baca(String id) {
        tombolUpdate.setVisible(true);
        tombolTambah.setVisible(false);
        Connection conn;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/tokobuku","root", "");

            Statement st;
            st = conn.createStatement();
            ResultSet rs;
            rs = st.executeQuery("select * from konsumen where id='"+id+"'");
            if(rs.next()) {
                textId.setText(rs.getString("id"));
                textNama.setText(rs.getString("nama"));
                textTelepon.setText(rs.getString("telepon"));
            }
            st.close();
            rs.close();
            conn.close();
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null,"gagal baca");
        }
        setVisible(true);
    }    

