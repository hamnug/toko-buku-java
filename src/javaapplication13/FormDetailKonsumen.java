    private void tombolTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolTambahActionPerformed
        // TODO add your handling code here:
        Connection conn;
        try {
            // below two lines are used for connectivity.
            Class.forName("com.mysql.cj.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/tokobuku", "root", "");

            // baca data
            String id = textId.getText();
            String nama = textNama.getText();
            String telepon = textTelepon.getText();

            // SQL
            String sql = "insert into konsumen (id, nama, telepon) values (?,?,?)";

            // siapkan statement untuk INSERT
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, id);
            pst.setString(2, nama);
            pst.setString(3, telepon);

            // eksekusi SQL
            pst.execute(); 

            // hapus objek 
            pst.close();
            conn.close();

            // tampilkan pesan
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        } catch(Exception e) {
            //JOptionPane.showMessageDialog(null,e.getMessage().toString());
            JOptionPane.showMessageDialog(null,e.getMessage().toString());
        } 
    }//GEN-LAST:event_tombolTambahActionPerformed

